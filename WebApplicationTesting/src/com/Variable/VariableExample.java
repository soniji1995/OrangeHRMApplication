package com.Variable;

public class VariableExample 
{
	public static void main(String []args)
	{
	
	byte byteVariable = 10;
	/*
	System.out.println("The value of byteVariable is:");
	System.out.println(byteVariable);
	*/
	System.out.println("This is the value assigned to byteVariable :- " + byteVariable);
	
	short shortVariable = 30;
	System.out.println("This is the value assigned to shortVariable :- " + shortVariable);
	
	int intVariable = 40;
	System.out.println("This is the value assigned to intVariable :- " + intVariable);
	
	long longVariable = 50;
	System.out.println("This is the value assigned to longVariable :- " + longVariable);
	
	double doubleValriable = 60.123456789123456;
	System.out.println("This is the value assigned to doubleValriable :- " + doubleValriable);
	
	float floatVariable = 60.12345678912341234f;
	System.out.println("This is the value assigned to floatVariable :- " + floatVariable);
	
	boolean booleanVariable = true;
	System.out.println("This is the value assigned to booleanVariable :- " + booleanVariable);
	
	char characterVariable = 'R';
	System.out.println("This is the value assigned to characterVariable :- " + characterVariable);
	
	String stringVariable = "Rahul";
	System.out.println("This is the value assigned to stringVariable :- " + stringVariable);
	
	
	}

}
