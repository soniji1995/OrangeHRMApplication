package com.Loops;

public class DoWhileLoopExample {
	
	public static void main(String[] args) {
		
		int var = 3;
		
		do {
			System.out.println(var+" Testing ");
			var++;
		}while(var>=5); //The do while loop executes atleast once without any condition check.
		                //Loop will terminate if condition found to be false
	}

}
